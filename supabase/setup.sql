    p_email text, 
    p_celular text, 
    p_plano_id integer, 
    p_is_admin boolean, 
    p_status text,
    p_data_compra text DEFAULT NULL,
    p_data_final_plano text DEFAULT NULL
)
RETURNS TABLE(success boolean, message text)
LANGUAGE plpgsql
SECURITY DEFINER
AS $function$
DECLARE
    current_user_id UUID;
    is_user_admin BOOLEAN := FALSE;
BEGIN
    -- Verificar se o usuário atual é admin
    current_user_id := auth.uid();
    
    SELECT usuarios.is_admin INTO is_user_admin
    FROM public.usuarios 
    WHERE usuarios.auth_user = current_user_id;
    
    IF NOT is_user_admin THEN
        RAISE EXCEPTION 'Acesso negado: usuário não é administrador';
    END IF;
    
    -- Atualizar usuário
    UPDATE public.usuarios 
    SET 
        nome = p_nome,
        email = p_email,
        celular = p_celular,
        plano_id = p_plano_id,
        data_compra = CASE 
            WHEN p_data_compra IS NOT NULL AND p_data_compra != '' 
            THEN p_data_compra::date::timestamp with time zone 
            ELSE data_compra 
        END,
        data_final_plano = CASE 
            WHEN p_data_final_plano IS NOT NULL AND p_data_final_plano != '' 
            THEN p_data_final_plano::date::timestamp with time zone 
            ELSE data_final_plano 
        END,
        is_admin = p_is_admin,
        status = p_status,
        ultima_atualizacao = NOW()
    WHERE id = p_user_id;
    
    IF NOT FOUND THEN
        RETURN QUERY SELECT false, 'Usuário não encontrado';
        RETURN;
    END IF;
    
    RETURN QUERY SELECT true, 'Usuário atualizado com sucesso';
END;
