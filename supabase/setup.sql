        ELSE 0
      END
    ), 0)
    FROM public.usuarios u
    LEFT JOIN public.planos_sistema ps ON u.plano_id = ps.id
    WHERE ps.tipo_periodo != 'free' OR ps.tipo_periodo IS NULL) as receita_estimada_mensal;
