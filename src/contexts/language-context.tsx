export function LanguageProvider({ children }: { children: ReactNode }) {
  const [language, setLanguageState] = useState<Language>('pt');
  const [isDetecting, setIsDetecting] = useState(false);
  const { profile } = useUser();

  // Sincronizar idioma com perfil do usuário
  useEffect(() => {
    if (profile?.idioma && ['pt', 'es', 'en'].includes(profile.idioma)) {
      setLanguageState(profile.idioma as Language);
      localStorage.setItem('language', profile.idioma);
    }
  }, [profile]);

  // Detectar idioma: localStorage > idioma_padrao_planos (admin) > navegador > pt
  useEffect(() => {
    const detectLanguage = async () => {
      try {
        // 1. Verificar se já tem idioma salvo no cache
        const cachedLang = localStorage.getItem('language') as Language;
        if (cachedLang && ['pt', 'es', 'en'].includes(cachedLang)) {
          setLanguageState(cachedLang);
          return;
        }

        setIsDetecting(true);

        // 2. Buscar idioma padrão configurado pelo admin
        try {
          const supabase = createClient();
          const { data } = await supabase
            .from('configuracoes_sistema')
            .select('idioma_padrao_planos')
            .eq('id', 1)
            .single();

          if (data?.idioma_padrao_planos && ['pt', 'es', 'en'].includes(data.idioma_padrao_planos)) {
            localStorage.setItem('language', data.idioma_padrao_planos);
            setLanguageState(data.idioma_padrao_planos as Language);
            return;
          }
        } catch {
          // Se falhar a busca, continua para fallback do navegador
        }

        // 3. Fallback: detectar pelo idioma do navegador
        const browserLang = navigator.language || (navigator as any).userLanguage;
        let detectedLang: Language = 'pt';

        if (browserLang.startsWith('es')) {
          detectedLang = 'es';
        } else if (browserLang.startsWith('en')) {
          detectedLang = 'en';
        } else if (browserLang.startsWith('pt')) {
          detectedLang = 'pt';
        }

        localStorage.setItem('language', detectedLang);
        setLanguageState(detectedLang);

      } catch (error) {
        setLanguageState('pt');
      } finally {
        setIsDetecting(false);
      }
    };

    detectLanguage();
  }, []);

  const setLanguage = (lang: Language) => {
    setLanguageState(lang);
    if (typeof window !== 'undefined') {
      localStorage.setItem('language', lang);
    }
  };

  const t = (key: string): string => {
    const keys = translations[language];
    return keys[key as keyof typeof keys] || key;
  };

  return (
    <LanguageContext.Provider value={{ language, setLanguage, t }}>
      {children}
    </LanguageContext.Provider>
  );
}

export function useLanguage() {
  const context = useContext(LanguageContext);
  if (!context) {
    throw new Error('useLanguage must be used within a LanguageProvider');
  }
  return context;
}
